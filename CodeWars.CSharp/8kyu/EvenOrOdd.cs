namespace CodeWars.CSharp._8kyu
{
    // Even or Odd
    // https://www.codewars.com/kata/53da3dbb4a5168369a0000fe
    public class EvenOrOdd
    {
        public class SolutionClass
        {
            public static string EvenOrOdd(int number)
            {
                return number % 2 == 0 ? "Even" : "Odd";
            }
        }
    }
}
